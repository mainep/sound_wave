export class WaveData {
  constructor(offset, speed, totalCurves, curveWidth, curveHeight, ease, line) {
    this.offset = offset;
    this.speed = speed;
    this.totalCurves = totalCurves;
    this.curveWidth = curveWidth;
    this.curveHeight = curveHeight;
    this.ease = ease;
    this.line = line;
  }
}
